var postorderTraversal = function (root) {
  let returnArr = [];
  postTravel(root, returnArr);
  return returnArr;
};
var postTravel = function (root, num) {
  if (root === null) return;
  if (root.left) postTravel(root.left, num);
  if (root.right) postTravel(root.right, num);
  num.push(root.val);
};

